package operation;

public class CostVo {
	String pr_name;
	String robot_cost;
	String profit_rate;
	
	public String getPr_name() {
		return pr_name;
	}
	public void setPr_name(String pr_name) {
		this.pr_name = pr_name;
	}
	public String getRobot_cost() {
		return robot_cost;
	}
	public void setRobot_cost(String robot_cost) {
		this.robot_cost = robot_cost;
	}
	public String getProfit_rate() {
		return profit_rate;
	}
	public void setProfit_rate(String profit_rate) {
		this.profit_rate = profit_rate;
	}
}
